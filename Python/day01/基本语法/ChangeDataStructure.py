# 演示了Python中的数据类型转换
test = 3.1415926
test_int = int(test)
test_str = str(test)
test_bool = bool(test)
test_float = float(test)
print(type(test_int),test_int)
print(type(test_str),test_str)
print(type(test_bool),test_bool)
print(type(test_float),test_float)
